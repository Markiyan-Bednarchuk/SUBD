SELECT Телефон FROM Пасажир
WHERE Імя_пасажира='Ігор' AND Прізвище_пасажира='Качка'

SELECT Імя_пасажира, Прізвище_пасажира, Паспортні_дані, Електронна_адреса
FROM Пасажир as П
LEFT JOIN Вагон AS В ON П.Код_пасажира = В.Код_вагона

SELECT * FROM Вагон
WHERE NOT EXISTS
(SELECT Код_пасажира FROM Пасажир
WHERE Пасажир.Код_пасажира = Вагон.Код_вагона)

SELECT Прізвище_пасажира,Імя_пасажира FROM Пасажир, Вагон
WHERE Пасажир.Код_пасажира = Вагон.Код_вагона
AND Тип_вагона = 'Перший клас'
